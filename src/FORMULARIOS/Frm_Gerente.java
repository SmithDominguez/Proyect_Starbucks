    private void cargarReclamos(){
        modeloTabla.setRowCount(0);
        
        for(Reclamo r : Principal.listaReclamos){
            modeloTabla.addRow(new Object[]{
                r.getCodigo(),
                r.getDNICliente(),
                r.getFecha(),
                r.getTipo(),
                r.getObservacion()
            });
        }
    } 
    private void cargarClientes(){
        tablaClientes.setRowCount(0);
        
        for(Cliente r : Principal.listaClientes){
            tablaClientes.addRow(new Object[]{
                r.getDNI(),
                r.getNombreCliente(),
            });
        }
    } 
    private void registrarReclamo(){
        Date fecha = DateChooser.getDate();
        String DNI = txtDNICliente.getText();
        String tipo = comboTipo.getSelectedItem().toString();
        String observacion = txtObservacion.getText();
        
        Principal.listaReclamos.add(new Reclamo(codigo++, DNI, fecha, tipo, observacion));
        System.out.println("Registrado Reclamo correctamente");
        
        cargarReclamos();
        limpiarCamposCliente();
    }
    private void registrarCliente(){
        String DNI = txtDniClienteRegistrar.getText();
        String Nombre = txtNombreClienteRegistrar.getText();
        
        Principal.listaClientes.add(new Cliente(DNI, Nombre));
        System.out.println("Registrado Cliente correctamente");
        cargarClientes();
        limpiarCampos();
    }
    private void limpiarCampos(){
        txtDNICliente.setText("");
        txtNombreCliente.setText("");
        comboTipo.setSelectedIndex(0);
        txtObservacion.setText("");    
    }
    private void limpiarCamposCliente(){
        txtDniClienteRegistrar.setText("");
        txtNombreClienteRegistrar.setText("");   
    }
